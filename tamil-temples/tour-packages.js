// Tour Package Renderer
(function () {
  var type = document.body.dataset.sectionType;
  if (!type) return;

  var packages = window.tourPackages[type];
  if (!packages || !packages.length) return;

  var container = document.getElementById('templeList');
  if (!container) return;

  var section = document.createElement('section');
  section.className = 'tour-packages-section';
  section.innerHTML =
    '<div class="tour-packages-header">' +
      '<h2>Pilgrimage Tour Packages</h2>' +
      '<p>Curated packages from popular tour operators for this temple circuit.</p>' +
    '</div>' +
    '<div class="tour-packages-grid">' +
      packages.map(function (pkg) {
        return '<article class="tour-package-card">' +
          '<div class="tour-pkg-header">' +
            '<h3>' + pkg.operator + '</h3>' +
            '<span class="tour-pkg-price">' + pkg.price + '</span>' +
          '</div>' +
          '<div class="tour-pkg-meta">' +
            '<span class="tour-pkg-duration">' + pkg.duration + '</span>' +
            '<span class="tour-pkg-temples">' + pkg.temples + '</span>' +
          '</div>' +
          '<ul class="tour-pkg-highlights">' +
            pkg.highlights.map(function (h) { return '<li>' + h + '</li>'; }).join('') +
          '</ul>' +
          '<a href="' + pkg.url + '" target="_blank" rel="noopener noreferrer" class="tour-pkg-btn">View Package</a>' +
        '</article>';
      }).join('') +
    '</div>';

  container.parentNode.insertBefore(section, container.nextSibling);
})();
